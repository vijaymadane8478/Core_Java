class SwitchDemo{
	public static void main(String[]args){
		int num=3;
		System.out.println("Before switch case");
		switch(num){
			case 1 :
				System.out.println("One");
			case 2 :
				System.out.println("Two");
			case 3 :
				System.out.println("Three");
			case 4 :
				System.out.println("Four");
		}
		System.out.println("After Switch");
	}
}
